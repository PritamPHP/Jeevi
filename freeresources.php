        <h3> Free Resources </h3>
 <p>Although JEEVI serves people from diverse backgrounds, with different needs, here is the list of the national services especially providing assistance to the people with their mental health.</p>       
<p>Beyond Blue aims to increase awareness of depression and anxiety and reduce stigma. Call 1300 22 4636, 24 hours/7 days a week, chat online or email.</p>
<p>Blue Knot Foundation Helpline is the National Centre of Excellence for Complex Trauma. It provides support, education and resources for the families and communities of adult survivors of childhood trauma and abuse. Call 1300 657 380, Monday – Sunday between 9am – 5pm AEST or via email helpline@blueknot.org.au.</p>
<p>Butterfly Foundation's National Helpline is a free, confidential service that provides information, counselling and treatment referral for people with eating disorders, and body image and related issues. Call 1800 33 4673, 8am-midnight AEST / 7 days a week, chat online or email.</p>


<p>eheadspace provides free online and telephone support and counseling to young people 12 – 25 and their families and friends. Call 1800 650 890, 9am – 1am AEST / 7 days a week, chat online or email.</p>

<p>FriendLine supports anyone who's feeling lonely, needs to reconnect or just wants a chat. You can call them 7 days a week on 1800 424 287, or chat online with one of their trained volunteers. All conversations with FriendLine are anonymous.</p>

<p>Kids Helpline is Australia’s  free 24/7 confidential and private counseling service specifically for children and young people aged 5 – 25. Call 1800 55 1800.</p>

<p>Lifeline provides 24-hour crisis counselling, support groups and suicide prevention services. Call 13 11 14, text on 0477 13 11 14 (12pm to midnight AEST) or chat online.</p>

<p>MensLine Australia is a professional telephone and online counseling service offering support to Australian men. Call 1300 78 99 78, 24 hours/7 days a week, chat online or organize a video chat.</p>

<p>MindSpot is a free telephone and online service for people with anxiety, stress, low mood or depression. It provides online assessment and treatment for anxiety and depression. MindSpot is not an emergency or instant response service. Call 1800 61 44 34.</p>

<p>QLife provides nationwide telephone and web-based services for lesbian peer support and referral for people wanting to talk about a range of issues including sexuality, identity, gender, bodies, feelings or relationships. Call 1800 184 527, 3pm – 12am (midnight) AEST/7 days a week.</p>
<p>PANDA (Perinatal Anxiety & Depression Australia) supports women, men and families across Australia affected by anxiety and depression during pregnancy and in the first year of parenthood. Call 1300 726 306, 9am – 7:30pm AEST (Mon-Fri).</p>

<p>SANE Australia provides support to anyone in Australia affected by complex mental health issues, as well as their friends, family members and health professionals. Call 1800 18 7263, 10am – 10pm AEST (Mon – Fri), or chat online.</p>

<p>Suicide Call Back Service provides 24/7 support if you or someone you know is feeling suicidal. Call 1300 659 467.</p>

<p>Open Arms — Veterans and Families Counseling provides 24/7 free and confidential counseling to anyone who has served at least one day in the ADF, their partners and families. Call 1800 011 046.</p>

<p>Head to Health provides free advice, assessment and referral into local mental health services. Call 1800 595 212 between 8:30am to 5pm on weekdays (public holidays excluded).
Source: <a href="https://www.healthdirect.gov.au/">HealthDirect.gov.au</a></p>
    
    
