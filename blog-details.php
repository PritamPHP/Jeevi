                        <p><strong><em>Simple yoga breath exercise</em></strong></p>
                        <ol>
                        <li>Take a deep breath in and hold it for a count of three.</li>
                        <li>Breathe out forcefully, like you’re blowing out a candle.</li>
                        <li>Repeat this for five cycles of breath.</li>
                        </ol>
                        <p><strong><em>Flying bird breath</em></strong></p>
                        <ol>
                        <li>Stand tall, with arms at your sides and feet hip-width apart in standing Mountain Pose.</li>
                        <li>Imagine being a beautiful, strong bird.</li>
                        <li>Pretend to prepare to fly by inhaling and raising your arms (“wings”) until your palms touch overhead. Keep your arms straight.</li>
                        <li>Exhale slowly as you bring your arms back down to your sides, palms facing down.</li>
                        <li>Repeat in a steady motion with each breath: inhale as you raise your arms, and exhale as lower your arms.</li>
                        <li>Optional: Close your eyes as you repeat the movements with breath, and imagine yourself flying in the sky like a bird.</li>
                        </ol>
                        <h3>Yoga games</h3>
                        <p><strong><em>Mirror, mirror</em></strong><strong>. </strong>This game is a good warm-up exercise to increase focus.</p>
                        <ol>
                        <li>One person starts as the leader. The leader chooses a pose to do and shows it to the others.</li>
                        <li>The other players copy the leader’s pose as if they are looking into a mirror.</li>
                        <li>Change the leader with each round of poses, so that everyone has a turn at being the leader.</li>
                        </ol>
                        <p><strong><em>Yogi says</em></strong></p>
                        <p>One person is selected as the Yogi. The other players must do the yoga poses that the Yogi tells them to do if the instruction starts with “Yogi says.” If the Yogi doesn’t use “Yogi says,” then players do not do the pose. Keep changing the person who is Yogi, so that everyone gets a turn.</p>
                        <p><strong><em>Red light, green light yoga</em></strong></p>
                        <p>One person is chosen as the Stoplight. He or she stands at the front of the room. The other players are the “cars,” and they start at the opposite wall. The Stoplight starts the game by calling “Green light!” The other players then use yoga poses to move forward. When the Stoplight calls “Red light!,” each player needs to be in a yoga pose and remain still. Everyone takes a turn being the Stoplight.</p>
                        <h3>Meditation</h3>
                        <p>Meditation can be short and simple, and does not have to involve complex yoga poses or staying still in a quiet, dark room. One parent, who is also a physician, describes playing a “<span style="color: #3366ff;"><a style="color: #3366ff;" href="#">meditation game</a></span>” with her children before bedtime, when she turns off electronic devices and reflects on the day with her children, using questions like, “What are you grateful for today?”</p>
                        <p>Here are a few simple meditations for children, which can be done for as little as 30 seconds or for several minutes.</p>
                        <p><strong><em>Mindful awareness meditation</em></strong></p>
                        <ol>
                        <li>Find a comfortable seated position or lie down.</li>
                        <li>Close your eyes.</li>
                        <li>Try to listen to every single sound in the room.</li>
                        </ol>
                        <p><strong><em>Loving kindness meditation</em></strong></p>
                        <ol>
                        <li>Find a comfortable seated position or lie down.</li>
                        <li>Close your eyes and think about someone you love.</li>
                        <li>Hold them tight in your heart and continue to think about that person.</li>
                        </ol>

                      
