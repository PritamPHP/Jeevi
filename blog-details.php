                        <p>Yoga is becoming increasingly popular among American children. A national survey found that 3% of U.S. children (1.7 million) did yoga as of 2012 — that’s 400,000 more children than in 2007.</p>
                        <p>Yoga and mindfulness have been shown to improve both physical and mental health in school-age children (ages 6 to 12). Yoga improves <span style="color: #3366ff;"><a style="color: #3366ff;" href="#">balance</a></span>, strength, endurance, and <span style="color: #3366ff;"><a style="color: #3366ff;" href="#">aerobic capacity</a></span> in children. Yoga and <span style="color: #3366ff;"><a style="color: #3366ff;" href="http://www.ncbi.nlm.nih.gov/pubmed/25773322">mindfulness</a></span> offer psychological benefits for children as well. A growing body of research has already shown that yoga can improve focus, <a href="http://www.ncbi.nlm.nih.gov/pubmed/26491461">memory</a>, self-esteem, academic performance, and <span style="color: #3366ff;"><a style="color: #3366ff;" href="http://www.ncbi.nlm.nih.gov/pubmed/25412616">classroom behavior</a></span><u>,</u> and can even <span style="color: #3366ff;"><a style="color: #3366ff;" href="http://www.ncbi.nlm.nih.gov/pubmed/26565100">reduce anxiety and stress</a></span> in children.</p>
