                        <p>Emerging research studies also suggest that yoga can help children with attention deficit hyperactivity disorder (ADHD) by improving the <span style="color: #3366ff;"><a style="color: #3366ff;" href="http://www.ncbi.nlm.nih.gov/pubmed/25988743">core symptoms of ADHD</a></span>, including inattentiveness, hyperactivity, and impulsivity. It can also boost <span style="color: #3366ff;"><a style="color: #3366ff;" href="http://www.ncbi.nlm.nih.gov/pubmed/23316384">school performance</a></span> in children with ADHD. A growing number of schools now integrate yoga and mindfulness into physical education programs or classroom curriculums, and many yoga studios offer classes for school-age children. Yoga can be playful and interactive for parents and children at home, as well.</p>
                        <p>Jessica Mei Gershen, a certified yoga instructor who teaches yoga to children at Brooklyn Yoga Project and founder of <span style="color: #3366ff;"><a style="color: #3366ff;" href="http://www.yogaforallneeds.com/">Yoga For All Needs</a></span>, recommends making yoga playful and fun for kids, whether in the classroom or at home. In her yoga classes, Gershen weaves in fun games and stories with positive themes like compassion, gratitude, and strength.</p>
                        <p>“Yoga is really effective because it’s so tangible. Learning physical postures builds confidence and strength as well as the mind-body connection,” Gershen says. She also has found that the effects of yoga go beyond physical fitness and also allow kids to build confidence and awareness beyond the classroom. “Through yoga, kids start to realize that they are strong and then are able to take that strength, confidence, acceptance, and compassion out into the world,” notes Gershen.</p>
